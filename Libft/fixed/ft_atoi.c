#include "libft.h"

int	ft_atoi(const char *s)
{
	int	i;
	int	nb;

	i = 0;
	nb = 0;
	if (s[0] == '+' || s[0] == '-' || (s[0] <= '9' && s[0] >= '0'))
	{
		if (s[0] == '+' || s[0] == '-')
		{
			i = 1;
		}
		while (s[i] != '\0')
		{
			nb = nb * 10 + (s[i] - 48);
			i++;
		}
		if (s[0] == '-')
		{
			return (-1 * nb);
		}
		return (nb);
	}
	return (0);
}

